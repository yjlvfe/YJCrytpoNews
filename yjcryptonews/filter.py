"""فلترة ذكية للأخبار — تقييم الأهمية والتأثير على الأسواق المالية"""
from . import config, ai as ai_client
from .log import get_logger

logger = get_logger("filter")


def _call(messages: list, max_tokens: int = 4096) -> str | None:
    """فلترة عبر سلسلة مزودين متعددة — أقصى موثوقية"""
    return ai_client.call_with_multi_fallback(messages, max_tokens=max_tokens, temperature=0.15)


def _extract_json(text: str) -> dict | None:
    """استخراج JSON عبر العميل الموحد"""
    return ai_client.extract_json(text)


def rank_news(items: list, top_n: int = 3) -> list:
    """تقييم الأخبار واختيار الأكثر تأثيراً على الأسواق المالية عالمياً"""
    if not items:
        return []

    # بناء قائمة الأخبار (نزيد الحد لـ 50 عشان عندنا مصادر أكثر)
    lines = []
    for i, item in enumerate(items[:50]):
        s = (item.get("summary", "") or "")[:200]
        lines.append(f"[{i+1}] {item['title']} — {s}")
    items_text = "\n\n".join(lines)

    prompt_parts = [
        "أنت محلل أسواق مالية عالمي خبير ومتخصص. أمامك قائمة أخبار. مهمتك اختيار الأكثر تأثيراً على الأسواق المالية.",
        "",
        "✅ اختر فقط الأخبار التي لها تأثير **قوي ومباشر** على الأسواق المالية (من أي مجال):",
        "",
        "   🪙 **الكريبتو والعملات الرقمية:**",
        "   - تحركات بيتكوين وإيثريوم وأسعار العملات الرقمية",
        "   - تنظيمات وقوانين الكريبتو (SEC، موافقات ETFs، تراخيص)",
        "   - تدفقات مؤسسية (BlackRock, Fidelity، صناديق)",
        "   - اختراقات أمنية، عمليات احتيال كبرى",
        "   - تقنيات جديدة في البلوكشين و DeFi",
        "",
        "   📊 **الاقتصاد والأسواق التقليدية:**",
        "   - قرارات البنوك المركزية (الفيدرالي، المركزي الأوروبي، بنك اليابان)",
        "   - التضخم، أسعار الفائدة، الناتج المحلي، التوظيف",
        "   - تحركات الأسهم والمؤشرات الكبرى (S&P 500، ناسداك، داو جونز)",
        "   - أرباح الشركات الكبرى والتقارير الربعية",
        "   - أسعار النفط، الذهب، السلع",
        "   - عمليات الدمج والاستحواذ الكبرى",
        "   - بيانات اقتصادية كلية (GDP، CPI، PPI، الوظائف)",
        "",
        "   🏛️ **السياسة المؤثرة في الأسواق:**",
        "   - تغييرات ضريبية أو جمركية كبرى",
        "   - حروب أو نزاعات جيوسياسية ترفع/تخفض الأسعار",
        "   - قرارات حكومية تؤثر على قطاعات كاملة",
        "   - عقوبات اقتصادية دولية",
        "   - قوانين تنظيمية جديدة للأسواق والشركات",
        "",
        "   🤖 **التكنولوجيا المؤثرة في الأسواق:**",
        "   - إعلانات كبرى من شركات التكنولوجيا العملاقة (NVIDIA, Apple, Microsoft, Google, Meta)",
        "   - ابتكارات في الذكاء الاصطناعي والرقائق",
        "   - استثمارات ضخمة في التقنية",
        "   - اكتتابات كبرى (IPO)",
        "   - تطورات في الأمن السيبراني والمركزات البيانات",
        "",
        "❌ **ممنوع منعاً باتاً** اختيار الأخبار التالية حتى لو لها تأثير طفيف:",
        "   - أخبار رياضية، ترفيهية، فنية، ثقافية",
        "   - كوارث طبيعية (زلازل، أعاصير) بدون تأثير مالي مباشر",
        "   - أخبار محلية لشركات صغيرة جداً",
        "   - زواج أو ولادة أو وفاة شخصيات بدون تأثير مالي",
        "   - أخبار صحية أو طبية عامة",
        "   - تقارير عن الطقس أو المناخ بدون تأثير اقتصادي",
        "",
        f"4. اختر بالكتير {top_n} أخبار. إذا ما في ولا خبر مناسب، أخرج قائمة فارغة.",
        "5. لكل خبر، أعطه درجة تأثير من 1-10 حسب قوة تأثيره على الأسواق المالية:",
        "   - 9-10: تأثير عالمي كبير (حرب، أزمة مالية، قرار بنك مركزي، إطلاق تقنية ثورية)",
        "   - 7-8: تأثير واضح على قطاع أو سوق معين",
        "   - 5-6: تأثير متوسط، خبر مهم لكن ليس استثنائياً",
        "   - 1-4: تأثير بسيط، خبر عادي",
        "6. أخرج JSON فقط. ممنوع أي نص خارج JSON.",
        'مثال: {"selected": [{"index": 3, "score": 9}, {"index": 5, "score": 7}]}',
        "",
        '⚠️ إذا ما في أخبار ذات صلة، أخرج {"selected": []}',
        "",
        "القائمة:",
        items_text,
    ]
    prompt = "\n".join(prompt_parts)

    result = _call([
        {"role": "system", "content": "أنت محلل أسواق مالية خبير. تختار الأخبار الأكثر تأثيراً على الأسواق. أخرج JSON فقط."},
        {"role": "user", "content": prompt},
    ])

    if not result or not result.strip():
        logger.warning("AI لم يجب")
        return []

    # استخراج JSON
    data = _extract_json(result)
    if not data:
        logger.warning("لم نجد JSON صحيح في رد AI")
        return []

    selected = data.get("selected", [])
    if not selected:
        logger.warning("AI: لا توجد أخبار ذات صلة — تخطي الدورة")
        return []

    # ترتيب حسب درجة التأثير
    sorted_selected = sorted(selected, key=lambda x: x.get("score", 0), reverse=True)

    result_items = []
    for sel in sorted_selected:
        idx = sel.get("index", 0) - 1
        score = sel.get("score", 0)
        if 0 <= idx < len(items):
            item = dict(items[idx])
            item["_ai_score"] = score
            result_items.append(item)

    if not result_items:
        return []

    logger.info("الدرجات: %s", [(it["_ai_score"], it["source"], it["title"][:40]) for it in result_items])
    return result_items[:top_n]
